          <p class="section-text">
            Prigi was born from a simple dream: to bring back the taste of Indonesian home to the hustle and bustle of modern life. Founded in 2015 by a culinary couple from Yogyakarta, Prigi started as a small stall on a corner of the city with only five tables and one signature dish — homemade honey-grilled chicken.
            Over time, customer support and a love for authentic food drove Prigi to grow. Traditional recipes from the founders' families began to be served in a wider variety: from Javanese breakfasts, snacks such as fried bananas, to specialties such as nasi gandul and Chinese-Indonesian capcay.
          </p>

          <p class="section-text">
